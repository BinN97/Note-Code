#include<stdio.h>
int main(){
    double sa=0;
    double sum=0;
    printf("请输入:");
    scanf("%lg",&sa);
    double k=sa-3500;
    if(k<=0){
        sum=0;
    }
    else if(k<1500){
        sum=k*0.03;
    }else if(k<4500){
        sum=1500*0.03+(k-1500)*0.1;
    }
    else if(k<9000){
        sum=1500*0.03+3000*0.1+(k-4500)*0.2;
    }
    else if(k<35000){
        sum=1500*0.03+3000*0.1+4500*0.2+(k-9000)*0.25;
    }
    else if(k<55000){
        sum=1500*0.03+3000*0.1+4500*0.2+26000*0.25+(k-35000)*0.3;
    }
    else if(k<80000){
        sum=1500*0.03+3000*0.1+4500*0.2+26000*0.25+20000*0.3+(k-55000)*0.35;
    }
    else{
        sum=1500*0.03+3000*0.1+4500*0.2+26000*0.25+20000*0.3+25000*0.35+(k-80000)*0.45;
    }
    printf("应缴纳%lg元\n",sum);
    return 0;
}
